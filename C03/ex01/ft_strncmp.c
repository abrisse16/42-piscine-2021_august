int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 1;
	if (n == 0)
		return (0);
	while (*s1 && *s2 && (*s1 == *s2))
	{
		if (i == n)
			return (0);
		s1++;
		s2++;
		i++;
	}
	return (*s1 - *s2);
}
