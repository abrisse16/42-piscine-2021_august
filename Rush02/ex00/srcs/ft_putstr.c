#include "rush.h"

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] >= '0' && str[i] <= '9')
		i++;
	while (str[i] != '\n')
	{
		write(1, &str[i], 1);
		i++;
	}
}
